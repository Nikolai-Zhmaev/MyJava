package ru.geekbrains.MyPractice;

public class Item {

    int weight, price; // создадим поля для хранения веса и стоимости предмета.
    String name; // поле для хранения названия предмета.

    public Item(int weight, int price, String name) {
        this.weight = weight;
        this.price = price;
        this.name = name;
    }

}
